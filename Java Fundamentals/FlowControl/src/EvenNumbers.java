public class EvenNumbers {
    public static void main(String[] args) {
        // Loop to iterate through numbers from 23 to 57
        for (int i = 23; i <= 57; i++) {
            // Check if the number is even
            if (i % 2 == 0) {
                // Print the even number on a separate row
                System.out.println(i);
            }
        }
    }
}
